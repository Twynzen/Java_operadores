package operadores.incremento.decremento;

/**
 *
 * @author LuisF
 */
public class OperadoresIncrementoDecremento {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x = 5;
        x++; // x++ = x + 1
        x--; // x-- = x - 1 || ++x != x++
        System.out.println(x);
                
    }
    
}
